#include "ft_printf.h"

void	ft_putstr(char *s)
{
	int	i;

	i = 0;
	if (!s)
	{
		write(1, "(null)", 6);
		return (5);
	}
	while (s[i])
	{
		write(1, &s[i], 1);
		i++;
	}
	return (i - 1);
}
